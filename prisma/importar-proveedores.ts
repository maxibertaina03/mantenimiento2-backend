function limpiar(v: string): string | null {
  const t = (v ?? '').trim();
  if (!t || t === '-') return null;
  return t;
}

async function main() {
  console.log('📦 Importando proveedores...');

  const filas = DATOS.split('\n')
    .map((l) => l.trim())
    .filter((l) => l.length > 0)
    .map((l) => {
      const [nombre, cuit, telefono, email, notas] = l.split('|');
      return {
        nombre: (nombre ?? '').trim(),
        cuit: limpiar(cuit),
        telefono: limpiar(telefono),
        email: limpiar(email),
        notas: limpiar(notas),
      };
    })
    .filter((p) => p.nombre.length > 0);

  // Únicos por nombre (evita duplicados exactos).
  const porNombre = new Map<string, (typeof filas)[number]>();
  for (const f of filas) if (!porNombre.has(f.nombre)) porNombre.set(f.nombre, f);
  const unicos = [...porNombre.values()];

  // No reinsertar los que ya existan por nombre (idempotente).
  const yaExisten = new Set(
    (await prisma.proveedor.findMany({ select: { nombre: true } })).map((p) => p.nombre),
  );
  const aInsertar = unicos.filter((p) => !yaExisten.has(p.nombre));

  if (aInsertar.length > 0) {
    await prisma.proveedor.createMany({ data: aInsertar });
  }

  console.log(`✅ Filas en la lista: ${filas.length}`);
  console.log(`✅ Únicos por nombre: ${unicos.length}`);
  console.log(`✅ Insertados ahora: ${aInsertar.length} (ya existían: ${unicos.length - aInsertar.length})`);
  console.log(`✅ Total de proveedores en la DB: ${await prisma.proveedor.count()}`);
}

main()
  .catch((e) => {
    console.error('❌ Error:', e);
    process.exit(1);
  })
